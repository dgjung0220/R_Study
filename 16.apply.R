x <- c(9:1,1:9)
xar <- array(x,c(3,3,2))
# , , 1

#      [,1] [,2] [,3]
# [1,]    9    6    3
# [2,]    8    5    2
# [3,]    7    4    1

# , , 2

#      [,1] [,2] [,3]
# [1,]    1    4    7
# [2,]    2    5    8
# [3,]    3    6    9

xar[,,1]
#      [,1] [,2] [,3]
# [1,]    9    6    3
# [2,]    8    5    2
# [3,]    7    4    1

# 행 기준으로 sum
apply(xar[,,1],1,sum) # 18 15 12
# 열 기준으로 sum
apply(xar[,,1],2,sum) # 24 15 6

apply(xar, 1, sum) # 30 30 30
apply(xar, 2, sum) # 30 30 30

# 행과 열을 기준으로 sum
# 두 매트릭스의 합
apply(xar, c(1,2), sum)
#       [,1] [,2] [,3]
# [1,]   10   10   10
# [2,]   10   10   10
# [3,]   10   10   10

apply(xar, c(2,3), sum)
#       [,1] [,2]
# [1,]   24    6
# [2,]   15   15
# [3,]    6   24

apply(xar, c(1,3), sum)
#       [,1] [,2]
# [1,]   18   12
# [2,]   15   15
# [3,]   12   18

apply(xar, c(1,2,3), sum)
# = xar